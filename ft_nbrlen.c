int	ft_nbrlen(int nb)
{
	int	len;

	if (nb == -2147483648)
		return (11);
	len = (nb < 0 ? 2 : 1);
	nb *= (nb < 0 ? -1 : 1);
	while (nb > 9)
	{
		nb /= 10;
		len++;
	}
	return (len);
}
