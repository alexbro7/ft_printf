#include "ft_printf.h"

void	ft_put_mod(t_pf *pf)
{
	int		c;

	c = (pf->lz ? '0' : ' ');
	if (pf->rl == 0)
		ft_put_x_char(pf->len - 1, c);
	write(1, "%", 1);
	if (pf->rl == 1)
		ft_put_x_char(pf->len - 1, ' ');
	pf->ret += pf->len;
	pf->ret += (pf->len == 0 ? 1 : 0);
}
